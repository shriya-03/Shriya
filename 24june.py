def abs_list(nos):
    return [abs(x) for x in nos]

print(abs_list([3, -7, 0, -2]))  # Output: [3, 7, 0, 2]

#function called abs_list , parameter: nos
#abs(x) is a built-in Python function.
#For each number x in the list, it applies abs(x) and creates a new list of those values.

